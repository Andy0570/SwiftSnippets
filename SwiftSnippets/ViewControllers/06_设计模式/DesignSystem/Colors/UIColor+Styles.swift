import UIKit
import SwifterSwift

extension UIColor {
    class var mainText: UIColor {
        return SFColor(hex: "#333333")
    }

    class var captionText: UIColor {
        return SFColor(hex: "#888888")
    }

    class var placeholder: UIColor {
        return SFColor(hex: "#EFEFEF")
    }
}
